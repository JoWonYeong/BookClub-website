let rent = document.querySelector('.rent');

책.forEach((a, i) => {
  // img 요소 만듦 (src에 url, alt에 책제목)
  let 책_img = document.createElement('img');
  책_img.setAttribute('src', a.url);
  책_img.setAttribute('alt', a.title);

  // span.title 요소 만듦 (innerHTML = title)
  let 책_title = document.createElement('span');
  책_title.classList.add('title');
  책_title.innerHTML = a.title;

  // span.content 요소 만듦(innerHTML = content)
  let 책_content = document.createElement('span');
  책_content.classList.add('content');
  책_content.innerHTML = a.content;

  // input.checked 요소 만듦(type='checkbox' name='book' value='book'+id)
  let 책_checked = document.createElement('input');
  책_checked.classList.add('checked');
  책_checked.setAttribute('type', 'checkbox');
  책_checked.setAttribute('name', 'book');
  책_checked.setAttribute('value', 'book' + i);

  // article.rent-book에 다 붙이기
  let article = document.createElement('article');
  article.classList.add('rent-book');
  article.appendChild(책_img);
  article.appendChild(책_title);
  article.appendChild(책_content);
  article.appendChild(책_checked);

  rent.appendChild(article);
});
